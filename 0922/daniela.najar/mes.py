#PROGRAMA PARA SABER EL MES DEL AÑO

mes = eval(input("ingrese el número del mes a evaluar: "))

if mes == 1: print("Su mes en enero")
elif mes == 2: print("Su mes en febrero")
elif mes == 3: print("Su mes en marzo")
elif mes == 4: print("Su mes en abril")
elif mes == 5: print("Su mes en mayo")
elif mes == 6: print("Su mes en junio")
elif mes == 7: print("Su mes en julio")
elif mes == 8: print("Su mes en agosto")
elif mes == 9: print("Su mes en septiembre")
elif mes == 10: print("Su mes en octubre")
elif mes == 11: print("Su mes en noviembre")
else: print("Su mes en diciembre")









